                  <p>
                  <p><img src="img/bibit.jpg" width="500" height="300" alt=""/>                                    
                  <ul>
                    <li>
                      <h4>Bibit Generatif</h4>
                    </li>
                    <p><br>
                      <br>
                      Perbanyakan tanaman menggunakan biji di sebut pembibitan secara generatif atau seksual. Karena biji berasal dari pertumbuhan embrio hasil penyerbukan (perkawinan, pembuahan) antara putik dengan serbuk sari..</p>
                    <p>Bibit generatif bisa berasal dari biji yang sengaja di benihkan atau yang tumbuh secara alamiah di alam. Perbanyakan pohon buah-buahan secara generatif sudah sejak dahulu ada dan di lakukan oleh orang -orang, hingga sekarang pun masih di gunakan. Bibit generatif memiliki kelemahan dan kelebihan tersendiri.</p>
                    <h4>Kelemahan Bibit Generatif:</h4>
                    <ol>
                      <li>tanaman sangat lama menghasilkan buah</li>
                      <li>Kualitas buah baru dapat di ketahui setelah tanaman berbuah</li>
                      <li>sifat baik seperti induknya sulit di peroleh karena biji nya merupakan hasil penyerbukan dengan tanaman lain.</li>
                      <li>Kemungkinan terburuk, tanaman bisa tidak menghasilkan buah sama sekli sepanjang hidupnya jika hanya berbunga jantan</li>
                    </ol>
                    <h4>Kelebihan Bibit Generatif :</h4>
                    <ol>
                      <li>pembibitan dapat di lakukan dengan mudah dan murah</li>
                      <li>kemungkinan menghasilkan varietas baru yang lebih baik</li>
                      <li>tanaman tumbuh sehat, kekar, kuat dan berumur panjang</li>
                      <li>Kalau kebetulan biji bersifat poliembrional, sifat-sifat tanaman baru bisa persis sama dengan pohon induk nya</li>
                    </ol>
                    <p>Pohon yang di kembangkan dari bibit generatif memerlukan waktu lama untuk berbuah karena proses pertumbuhan tanaman di awali dengan pembentukan janin terlebih dahulu. Selanjut nya di susul dengan pembentukan akar tunggang, akar serabut, batang, dan daun. Pertumbuhan tanaman di awali dari yang sama sekali tidak ada menjadi ada.</p>
                    <p>Pertumbuhan tanaman akan berlangsung lebih lama lagi kalau biji yang di tanam mengalami masa dorman. Saat Dorman, biji tak mengalami kegiatan sama sekali selama berminggu-minggu, berbulan-bulan, bahkan bertahun-tahun sampai benih mendapatkan situasi dan kondisi yang cocok untuk pertumbuhan nya. Masa dorman untuk biji buah-buahan seperti jeruk, durian, mangga, rata-rata sekitar 1 bulan. Pepaya masa dorman nya sekitar 1-2 minggu.<br>
                      Perbanayakan bibit secara generatif bisa di lakukan kalau tanaman hanya mungkin di perbanyak secara generatif saja. Cara ini sangat lazim di lakukan untuk pepaya, semangka, nangka, dan kelapa.Tanaman lain seperti manggis dan duku, juga sering menggunakan biji.</p>
                    <p>&nbsp;</p>
                  </ul>
                  <ul>
                    <li>
                      <h4>Bibit Vegetatif</h4>
                    </li>
                    <p><br>
                      <br>
                      Perbanyakan tanaman yang tidak menggunakan biji di sebut perbanyakan vegetatif atau aseksual. Bagian tanaman buah yang dapat di gunakan untuk perbanyakan ini adalah akar dan batang.<br>
                      Perbanayakan tanaman dengan bibit kultur jaringan sebenar nya juga termasuk perbanyakan tanaman secara vegetatif, karena bibit itu di tumbuhkan dari bagian sel tanaman yang tidak di buahi.<br>
                    </p>
                    <h4>Keistimewaan bibit vegetatif antara lain :</h4>
                    <ol>
                      <li>sifat-sifat unggul dari induk dapat di warisi oleh keturunan berikut nya</li>
                      <li>tanaman cepat berbunga dan berbuah</li>
                      <li>Tanaman dapat tumbuh baik di lahan yang permukaan air tanah nya dangkal</li>
                    </ol>
                    <h4>Kelemahan bibit Vegetatif antara lain:</h4>
                    <ol>
                      <li>jumlah bibit yang di peroleh dari pohon induk sedikit</li>
                      <li>Pohon mudah tumbang karena dangkal perakarannya, dan tidak tahan kekeringan pada saat musim kemarau</li>
                      <li>Sifat-sifat jelek pohon induk tetap terbawa</li>
                    </ol>
                    <p>Pohon buah-buahan yang erasal dari bibit vegetatif cepat menghasilkan buah, karena masa pertumbuhan vegetatif tanaman pendek. Mangga yang berasal dari biji pertama kali berbuah pada umur 6-7 Tahun. Kalau tanaman berasal dari bibi vegetatif, tanaman sudah bisa menghasilkan buah pertama pada usia 3 Tahun.</p>
                    <p>Belimbing dan jambu lebih cepat lagi menghasilkan buah, kalau bibit yang di pakai sudah berumur satu tahun sejak di lakukan perbanyakan vegetatif. Begitu di tanam d lahan, belimbing dan jamb dari bibit vegetataif segera memasuki fase reproduksi.</p>
                    <p>Masing-masing jenis pohon buah-buahan dapat di perbanyak secara vegetatif. Yang termasuk Vegetatif adalah <a title="setek" target="_blank" href="http://floris.blogbisnis.org/2014/03/setek.html">setek</a>, <a title="cangkok" target="_blank" href="http://floris.blogbisnis.org/2014/03/cangkok.html">cangkok</a>, <a title="rundukan" target="_blank" href="http://floris.blogbisnis.org/2014/03/rundukan.html">rundukan</a>, dan anakan (tunas)</p>
