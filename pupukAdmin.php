    <div class="content-section-a">   

        <div class="container">
            <div class="row">
            
                <div class="col-lg-10 col-sm-12">
                    <h2>PUPUK</h2>
                  <hr class="section-heading-spacer">
                  <div class="clearfix"></div>
                  <H3>Berbagai Macam Pupuk Kimia dan Kegunaannya</H3>
                    <p>&nbsp;</p>
              </div>
                <div class="col-lg-5 col-lg-offset-2 col-sm-9">
                  <p><img src="img/urea-bersusidi-300x233.jpg" width="300" height="233" alt=""/></p>
                  <p><strong>1. Urea (Amonium Carbamide)</strong></p>
                  <p>Deskripsi:</p>
                  <ol>
                    <li>Rumus kimia CO(NH2)2</li>
                    <li>Pupuk Urea adalah pupuk yang dibuat dari percampuran gas amoniak (NH3) dan gas asam arang (CO2).</li>
                    <li>Pupuk Urea mengandung 46% N. Jadi tiap 100 kg pupuk Urea mengandung 46 kg Nitrogen.</li>
                    <li>Pupuk Urea berbentuk kristal, warnanya beragam antara lain putih dan merah muda (bersubsidi).</li>
                  </ol>
                  <p>Sifat-sifat:</p>
                  <ol>
                    <li>Pupuk Urea bersifat higroskopis, sehingga pupuk urea ini mudah larut dalam air dan mudah diserap tanaman.</li>
                    <li>Reaksinya <strong><em>sedikit asam</em></strong>.</li>
                    <li>Selain mudah tercuci oleh air, juga mudah terbakar oleh sinar matahari.</li>
                  </ol>
                  <p>&nbsp;</p>
                  <p><img src="img/za-300x240.png" width="300" height="240" alt=""/></p>
                  <p><strong>2. ZA (Zwavelzure Amonium)</strong></p>
                  <p>Deskripsi:</p>
                  <ol>
                    <li>Rumus kimia (NH4)2SO4</li>
                    <li>Pupuk ZA adalah pupuk yang dibuat dari asam belerang (S) dan gas amonium (NH4+) sehingga disebut juga pupuk Amonium Sulfat.</li>
                    <li>Pupuk ZA mengandung 21% unsur hara makro N (nitrogen) sebagai kation amonium, dan 24 % unsur hara makro sekunder S (sulfur) sebagai anion sulfat. Jadi tiap 100 kg pupuk ZA mengandung 21 kg Nitrogen dan 24% Sulfur.</li>
                    <li>Pupuk ZA berbentuk kristal, warnanya beragam antara lain putih, merah muda, abu-abu, biru, ke abu-abuan dan kuning.</li>
                  </ol>
                  <p>Sifat-sifat:</p>
                  <ol>
                    <li>Pupuk ZA bersifat higroskopis (mudah menyerap uap air), terutama pada kelembapan 80 % atau lebih, sehingga mudah larut dalam air dan mudah diserap tanaman.</li>
                    <li>Reaksinya <strong><em>asam</em></strong>, sehingga kurang baik jika diberikan pada tanah muda (karena rata-rata tanah muda masih asam), atau tanah yang kurang mengandung kalsium (alkali).</li>
                    <li>Reaksi kerjanya agak lambat, sehigga cocok apabila digunakan sebagai pupuk dasar.</li>
                  </ol>
                  <p>&nbsp;</p>
                  <p><img src="img/sp36-300x240.png" width="300" height="240" alt=""/></p>
                  <p><strong>3. SP36 (Super Phospate)</strong></p>
                  <p>Deskripsi:</p>
                  <ol>
                    <li>Rumus kimia P2O5</li>
                    <li>Pupuk SP36 adalah pupuk yang dibuat dari asam sulfat (belerang) dan fosfat alam.</li>
                    <li>Pupuk SP36 mengandung 36% P dalam bentuk P2O5 (fosfat). Jadi tiap 100 kg pupuk SP36 mengandung 36 kg Fosfor (P).</li>
                    <li>Kadar P2O5 larut dalam air minimal 30 %.</li>
                    <li>Kadar air maksimal 5 %.</li>
                    <li>Pupuk SP36 berbentuk butiran besar, warnanya abu-abu.</li>
                  </ol>
                  <p>Sifat-sifat:</p>
                  <ol>
                    <li>SP36 bersifat tidak higroskopis, sehingga dapat disimpan lama walau kelembapan udara tinggi.</li>
                    <li>Reaksi kimianya tergolong <strong><em>netral</em></strong>.</li>
                    <li>Walau sifatnya mudah larut dalam air namun reaksinya lambat, sehingga sangat cocok digunakan untuk pupuk dasar pada tanaman semusim.</li>
                    <li>Mudah terbakar oleh sinar matahari.</li>
                  </ol>
                  <p>&nbsp;</p>
                  <p><img src="img/kcl-300x239.png" width="300" height="239" alt=""/></p>
                  <p><strong>4. KCl  (Kalium Klorida)</strong></p>
                  <p>Deskripsi:</p>
                  <ol>
                    <li>Rumus kimia KCl</li>
                    <li>Pupuk KCl adalah pupuk yang dibuat dari ekstraksi mineral Kalium.</li>
                    <li>Pupuk KCl mengandung 60% K dalam bentuk K2O.</li>
                    <li>Pupuk KCl berbentuk bubuk atau serbuk, warnanya merah.</li>
                  </ol>
                  <p>Sifat-sifat:</p>
                  <ol>
                    <li>Bersifat higroskopis, mudah larut dalam air dan mudah diserap tanaman.</li>
                    <li>Reaksinya <strong><em>netral sampai asam</em></strong>.</li>
                    <li>Unsur kloridanya bersifat toksik (racun) bagi tanaman tertentu seperti wortel dan kentang.</li>
                    <li>Dapat digunakan sebagai pupuk dasar atau pupuk susulan.</li>
                  </ol>
                  <p>&nbsp;</p>
                  <p> <img src="img/zk.jpg" width="239" height="191" alt=""/></p>
                  <p><strong>5. ZK  (Zwavelzure Kali)</strong></p>
                  <p>Deskripsi:</p>
                  <ol>
                    <li>Rumus kimia K2SO4</li>
                    <li>Pupuk ZK adalah pupuk yang dibuat dari asam belerang dan kalium, sehingga disebut juga pupuk Kalium Sulfat.</li>
                    <li>Pupuk ZK mengandung 50% unsur hara makro K dalam bentuk K2O dan 17 % unsur hara makro sekunder S (sulfur). Jadi tiap 100 kg pupuk ZK  mengandung 50 kg Kalium (K) dan 17 kg Sulfur (S).</li>
                    <li>Pupuk ZK berbentuk butiran kecil atau serbuk, warnanya putih.</li>
                    <li>Kadar klorida maksimal 2,5 %, kadar air maksimal 1%.</li>
                  </ol>
                  <p>Sifat-sifat:</p>
                  <ol>
                    <li>Pupuk ZK bersifat tidak higroskopis, sehingga dapat disimpan lama walau kelembapan udara tinggi.</li>
                    <li>Mudah larut dalam air, dan reaksinya <strong><em>netral sampai asam</em></strong>.</li>
                    <li>Sumber unsur kalium dengan kadar tinggi, khususnya untuk tanaman yang sensitif terhadap keracunan Klorida, seperti wortel dan kentang. Gunakan ZK, jangan KCl agar tanaman wortel dan kentang anda tidak keracunan unsur klorida.</li>
                    <li>Dapat digunakan sebagai pupuk dasar atau pupuk susulan.</li>
                  </ol>
