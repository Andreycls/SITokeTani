                  <h2>1. Tikus</h2>
                    <img src="img/tikus.PNG" width="410" height="314" alt=""/><BR>
Gejala serangan :<BR>
1. Tikus menyerang berbagai tumbuhan.<BR>
2. Menyerang di pesemaian, masa vegetatif, masa generatif, masa panen, tempat penyimpanan.<BR>
3. Bagian tumbuhan yang disarang tidak hanya biji-bijian tetapi juga batang tumbuhan muda.<BR>
4. Tikus membuat lubang-lubang pada pematang sawah dan sering berlindung di semak-semak.<BR>
<BR>
Pengendaliannya  :<BR>
<BR>
1. Membongkar dan menutup lubang tempat bersembunyi para tikus dan menangkap tikusnya.<BR>
2. Menggunakan musuh alami tikus, yaitu ular.<BR>
3. Menanam tanaman secara bersamaan agar dapat menuai dalam waktu yang bersamaan pula sehingga tidak ada kesempatan bigi tikus untuk mendapatkan makanan setelah tanaman dipanen.<BR>
4. Menggunakan rodentisida (pembasmi tikus) atau dengan memasang umpan beracun, yaitu irisan ubi jalar atau singkong yang telah direndam sebelumnya dengan fosforus. Peracunan ini sebaiknya dilakukna sebelum tanaman padi berbunga dan berbiji. Selain itu penggunaan racun harus hati-hati karena juga berbahaya bagi hewan ternak dan manusia.<BR>
<h2>
2. Wereng</h2>
<img src="img/wereng.PNG" width="470" height="205" alt=""/><BR>
Gejala serangan :<BR>
<BR>
1. Menyebabkan daun dan batang tumbuhan berlubang-lubang.<BR>
2. Daun dan batang kemudian kering, dan pada akhirnya mati.<BR>
<BR>
Pengendaliannya  :<BR>
<BR>
1. Pengaturan pola tanam, yaitu dengan melakukan penanaman secara serentak maupun dengan pergiliran tanaman. Pergiliran tanaman dilakukan untuk memutus siklus hidup wereng dengan cara menanam tanaman palawija atau tanah dibiarkan selama 1 - 2 bulan.<BR>
2. Pengandalian hayati, yaitu dengan menggunakan musuh alami wereng, misalnya laba-laba predator Lycosa Pseudoannulata, kepik Microvelia douglasi dan Cyrtorhinuss lividipenis, kumbang Paederuss fuscipes, Ophinea nigrofasciata, dan Synarmonia octomaculata.<BR>
3. Pengandalian kimia, yaitu dengan menggunakan insektisida, dilakukan apabila cara lain tidak mungkin untuk dilakukan. Penggunaan insektisida diusahakan sedemikan rupa sehingga efektif, efisien, dan aman bagi lingkungan.<BR>
<h2>
3. Walang Sangit</h2>
<img src="img/walang.PNG" width="462" height="288" alt=""/><BR>
Gejala serangan :<BR>
<BR>
1. Menghisap butir-butir padi yang masih cair.<BR>
2. Biji yang sudah diisap akan menjadi hampa, agak hampa, atau liat.<BR>
3. Kulit biji iu akan berwarna kehitam-hitaman.<BR>
4. Walang sangit muda (nimfa) lebih aktif dibandingkan dewasanya (imago), tetapi hewan dewasa dapat merusak lebih hebat karenya hidupnya lebih lama.<BR>
5. Walang sangit dewasa juga dapat memakan biji - biji yang sudah mengeras, yaitu dengan mengeluarkan enzim yang dapat mencerna karbohidrat.    Faktor - faktor yang mendukung yang mendukung populasi walang sangit antara lain sebagai berikut.<BR>
<BR>
1. Sawah sangat dekat dengat perhutanan.<BR>
2. Populasi gulma di sekitar sawah cukup tinggi.<BR>
3. Penanaman tidak serentak<BR>
<BR>
Pengendaliannya  :<BR>
<BR>
1. Menanam tanaman secara serentak.<BR>
2. Membersihkan sawah dari segala macam rumput yang tumbuh di sekitar sawah agar tidak menjadi tempat berkembang biak bagi walang sangit.<BR>
3. Menangkap walang sangit pada pagi hari dengan menggunakan jala penangkap.<BR>
4. Penangkapan menggunakan unmpan bangkai kodok, ketam sawah, atau dengan alga.<BR>
5. Melakukan pengendalian hayati dengan cara melepaskan predator alami beruba laba - laba dan menanam jamur yang dapat menginfeksi walang sangit.<BR>
6. Melakukan pengendalian kimia, yaitu dengan menggunakan insektisida.<BR>
<h2>
4. Ulat</h2>
<img src="img/ulat.PNG" width="462" height="250" alt=""/><BR>
Gejala serangan :<BR>
<BR>
1.Aktif memakan dedaunan bahkan pangkal batang, terutama pada malam hari.<BR>
2.Daun yang dimakan oleh ulat hanya tersisa rangka atau tulang daunya saja.<BR>
<BR>
Pengendaliannya  :<BR>
<BR>
1. Membuang telur - telur kupu - kupu yang melekat pada bagian bawah daun.<BR>
2. Menggenangi tempat persemaian dengan air dalam jumlah banyak sehingga ulat akan bergerak ke atas sehingga mudah untuk dikumpulkan dan dibasmi.<BR>
3. Apabila kedua cara diatas tidak berhasil, maka dapat dilakukan penyemprotan dengan menggunakan pertisida.</p>
                  <p>&nbsp;</p>
                  <h2>5. Tungau</h2>
                    <img src="img/tugau.PNG" width="458" height="315" alt=""/><BR>
Gejala serangan :<BR>
<BR>
1. Tungau (kutu kecil) bisaanya terdapat di sebuah bawah daun untuk mengisap daun tersebut.<BR>
2. Pada daun yang terserang kutu akan timbul bercak - bercak kecil kemudian daun akan menjadi kuning lalu gugur.<BR>
<BR>
Pengendaliannya  :<BR>
<BR>
1. Hama ini dapat diatasi dengan cara mengumpulkan daun - daun yang terserang hama pada suatu tempat dan dibakar.<BR>
<h2>
6. Lalat bibit</h2>
<img src="img/lalat.PNG" width="444" height="324" alt=""/><BR>
Gejala serangan :<BR>
<BR>
1. Lalat bibit meletakkan telur pada pelepah daun padi pada senja hari.<BR>
2. Telur menetas setelah dua hari dan larva merusak titik tumbuh. Pupa berwarna kuning kecoklatan terletak di dalam tanah. Setelah keluar dari pupa selama 1 minggu menjadi imago yang siap kawin.<BR>
3. Hama ini menyerang terutama pada kondisi kelembaban udara tinggi.<BR>
<BR>
Pengendaliannya  :<BR>
<BR>
1. Pengendaliannya diutamakan pada penanaman varitas yang tahan.<BR>
